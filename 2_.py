#primeiro foram declardas as variáveis da fórumula matemática para calcular a aréa de uma circunferência de um círculo
#utilizando o valor de "pi" como 3,14 e colocando o raio desejado podemos obter a área da circunferência
pi = 3.14
raio = 10        #pode ser utilizado qualquer valor no raio, alterando assim o resultado da área da circunferência
area = pi * raio  #fómula matemática utilizada para cálculo da circunferência
print (area)     #chamando o valor do cálculo
